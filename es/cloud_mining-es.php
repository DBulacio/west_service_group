            <h1>4 claves para entender la potencia del bitcoin</h1>
            <p>Hace ya varios años que las criptomonedas han cambiado la forma de manejar las transacciones electrónicas; en ese contexto, el bitcoin es cada vez más popular y su valor es cada vez mayor. Pero poco a poco se fue convirtiendo en uno de esos conceptos que todos conocen pero pocos saben explicar con profundidad. ¿Sabes reamente cómo se crea y cómo funciona?</p>
            <p>A continuación, te dejamos cinco preguntas y respuestas para que puedas entender <b> cómo opera esta criptomoneda </b> y por qué ha revolucionado el mundo del dinero digital.</p>
            
            <h3 class="lead">1. ¿Qué son las criptomonedas?</h3>
            <p>Son un tipo de moneda digital <b> basadas en la criptografía, </b> ya que emplean técnicas de cifrado para reglamentar la generación de unidades de moneda y verificar la transferencia de fondos, en reemplazo de una autoridad central.</p>
            <p>Las criptomonedas están altamente vinculadas con las matemáticas. A diferencia de las monedas tradicionales, que se imprimen en forma de monedas o billetes y suelen estar vinculadas a bienes físicos como el oro, una criptomoneda se produce mediante la resolución de <b> problemas matemáticos </b> basados en criptografía.</p>
            
            <h3 class="lead">2. ¿Qué es el bitcoin?</h3>
            <p>El bitcoin es la primer criptomoneda <b> descentralizada, </b> es decir que no depende de ningún gobierno o entidad financiera, en tener un valor en el mercado.</p>
            <p>El bitcoin fue creado en 2009 por <a href="https://bitcoin.org/bitcoin.pdf" target="_blank"> Satoshi Nakamoto </a> con el objetivo de poder hacer transacciones financieras digitales entre personas sin la necesidad de que intervenga un tercero, y que además estas <b> sean seguras, anónimas e inmediatas </b> a cualquier lugar del mundo.</p>
            <p>Algunas de las características más importantes del bitcoin son:</p>
            <li>Libre acceso: Cualquiera puede tener un bitcoin y participar en la red.</li>
            <li>Transferencias irreversibles: Una vez realizadas no se pueden deshacer ni cancelar.</li>
            <li>Anonimato: No se requiere identificación para participar en la red Bitcoin.</li>
            <li>Control total de tu dinero: puedes utilizar tu dinero libremente, en el momento que lo desees.</li>
            <li>Código abierto: el código fuente de Bitcoin siempre debe ser accesible para todos.</li>
            <p></p>

            <h3 class="lead">3. ¿Cómo funcionan las transacciones de bitcoins?</h3>
            <p>Si bien el bitcoin no está controlado por ningún gobierno, persona o entidad financiera, tiene que haber algo que le dé poder, confiabilidad y respaldo. Para esto, millones de personas (en realidad, computadoras y servidores) se encargan de mantener la criptomoneda funcionando: verifican las transacciones, llevan un <b> registro común </b> de transacciones y le brindan seguridad al sistema.</p>
            <p>Una explicación muy sencilla del investigador Morgen E. Peck es pensar el bitcoin como un registro contable digital.</p>
            <p>Imagina a un grupo de individuos alrededor de una mesa, cada uno frente a su laptop, y que todos tienen <b> acceso en tiempo real </b> al mismo registro contable. Este registro lleva la cuenta del número de bitcoins que tiene cada uno de estos individuos en todo momento.</p>
            <p>El saldo de cada cuenta es información pública, y si un individuo quiere transferir fondos a quien está sentado enfrente de él, debe anunciar esa transacción a todos los que están sentados a la mesa. Una vez anunciada la transacción, todo el grupo la añade al registro, para lo cual es necesario que <b> todos verifiquen la autenticidad </b> de dicha transacción.</p>
            <p>En un sistema como este, la moneda no existe en forma física, y sin embargo un individuo no puede gastar más de una vez una misma moneda (ya que si intenta incurrir en un doble gasto será detectado y rechazado por todos los demás).</p>
            <p>Así es, básicamente, cómo funciona el bitcoin, excepto que los participantes son computadoras que están distribuidas en una <b> red peer-to-peer global, </b> y todas las transacciones tienen lugar entre direcciones (billeteras) en lugar de individuos, sin revelar quiénes son sus respectivos dueños.</p>
            <p>La posesión de estas direcciones es verificada mediante <b> certificados digitales </b> basados en algoritmos asimétricos, es decir, mediante un sistema de llave pública y llave privada.</p>
            <p>Entonces, podemos decir que el bitcoin es un sistema protegido con criptografía en donde todos llevan las cuentas de todos.</p>

            <h3 class="lead">4. ¿Qué es la minería de bitcoins?</h3>
            <p>Minar bitcoins es el proceso de invertir capacidad de cómputo para procesar transacciones, garantizar la seguridad de la red y conseguir que todos los participantes estén sincronizados.</p>
            <p>El algoritmo de bitcoin permite llevar un registro colectivo de todas las transacciones a través de la red. Este registro público y compartido entre todas las partes se conoce como <b> blockchain. </b> En esta cadena todas las transacciones se registran con la fecha, hora, firma digital de los participantes y cantidad transferida.</p>
            <p>Sin embargo, varias transacciones ocurren al mismo tiempo, por lo que es necesario <b> acordar un orden </b> para mantener el registro sincronizado y actualizado entre todos los nodos de la red. Para esto, cada vez que se produce una transacción todos los nodos de la red verifican que sea viable y auténtica.</p>
            <p>Cada 10 minutos (aproximadamente) todos los nodos guardan en un bloque las transacciones válidas recibidas. Sin embargo, no todos los nodos agregan estas transacciones en el mismo orden, por lo que es necesario definir cuál es el bloque que se tomará como válido y se añadirá al registro (blockchain).</p>
            <p>Para esto, todos los nodos de la red participan de una especie de concurso. Consiste en encontrar el bloque que, sumado a un número aleatorio y aplicada una función criptográfica, dé por resultado un hash que cumple con una característica: tener una cierta cantidad de ceros a la izquierda.</p>
            <p>Dado que es imposible predecir el resultado de una función hash, cada minero debe calcular el hash para su bloque de transacciones sumado al número aleatorio <b> hasta dar con el resultado válido. </b> A medida que el número de ceros a la izquierda requerido para validar la información aumenta, la dificultad de encontrar un hash que cumpla con esta exigencia también aumenta.</p>
            <p>Si pensamos en el funcionamiento del blockchain, la minería consiste en utilizar los recursos del CPU y GPU para realizar estos cálculos matemáticos criptográficos, los cuales consumen gran cantidad de procesamiento.</p>
            <p>Cada vez que se logra resolver el cálculo de un bloque y el mismo es añadido a la cadena, se reparten <b> 12,5 bitcoins entre los mineros </b> que participaron en la obtención de ese bloque. De esta manera, se consigue que haya nodos motivados a trabajar en el procesamiento de las transacciones y también se introducen nuevas monedas al mercado.</p>
            <p>Aunque el proceso parece simple, la criptomoneda está diseñada de tal modo que la minería se va dificultando y su obtención requiere de mayor poder de cómputo y tiempo.</p>
            
