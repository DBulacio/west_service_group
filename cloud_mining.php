            <h1>4 keys to understanding the power of bitcoin</h1>
            <p>For several years now, cryptocurrencies have changed the way electronic transactions are handled; in this context, bitcoin is becoming more and more popular and its value is increasing. But little by little it has become one of those concepts that everyone knows about but few know how to explain in depth. Do you really know how it is created and how it works?</p>
            <p>Here are five questions and answers to help you understand <b>how this cryptocurrency operates</b> and why it has revolutionized the world of digital money.</p>
            
            <h3 class="lead">1. What are cryptocurrencies?</h3>
            <p>They are a type of digital currency <b>based on cryptography,</b> as they employ encryption techniques to regulate the generation of currency units and verify the transfer of funds, replacing a central authority.</p>
            <p>Cryptocurrencies are highly linked to mathematics. Unlike traditional currencies, which are printed in the form of coins or banknotes and are usually linked to physical assets such as gold, a cryptocurrency is produced by solving <b>mathematical problems</b> based on cryptography.</p>
            
            <h3 class="lead">2. What is bitcoin?</h3>
            <p>The bitcoin is the first <b>decentralized cryptocurrency,</b> meaning that it does not depend on any government or financial entity, to have a value in the market.</p>
            <p>Bitcoin was created in 2009 by <a href="https://bitcoin.org/bitcoin.pdf" target="_blank"> Satoshi Nakamoto</a> with the aim of being able to make digital financial transactions between people without the need for a third party to intervene, and also that these are <b> secure, anonymous and immediate</b> to anywhere in the world.</p>
            <p>Some of the most important features of bitcoin are:</p>
            <li>Free access: Anyone can have a bitcoin and participate in the network.</li>
            <li>Irreversible transfers: Once made, they cannot be undone or cancelled.</li>
            <li>Anonymity: No identification is required to participate in the Bitcoin network.</li>
            <li>Total control of your money: you can use your money freely, whenever you want.</li>
            <li>Open source: Bitcoin's source code must always be accessible to everyone.</li>
            <p></p>

            <h3 class="lead">3. How do bitcoin transactions work?</h3>
            <p>While bitcoin is not controlled by any government, person or financial entity, there has to be something to give it power, reliability and backing. For this, millions of people (actually, computers and servers) are in charge of keeping the cryptocurrency running: they verify transactions, keep a <b>common record</b> of transactions and provide security to the system.</p>
            <p>A very simple explanation from researcher Morgen E. Peck is to think of bitcoin as a digital accounting record.</p>
            <p>Imagine a group of individuals around a table, each in front of their laptop, and that they all have <b>real-time access</b> to the same accounting register. This register keeps track of the number of bitcoins held by each of these individuals at all times.</p>
            <p>The balance of each account is public information, and if an individual wants to transfer funds to whoever is sitting across from him, he must announce that transaction to everyone sitting at the table. Once the transaction is announced, the entire group adds it to the register, which requires <b>everyone to verify the authenticity</b> of that transaction.</p>
            <p>In such a system, currency does not exist in physical form, and yet an individual cannot spend the same currency more than once (since if he attempts to incur a double expenditure he will be detected and rejected by everyone else).</p>
            <p>This is basically how bitcoin works, except that the participants are computers that are distributed in a <b>global peer-to-peer network,</b> and all transactions take place between addresses (wallets) rather than individuals, without revealing who their respective owners are.</p>
            <p>The possession of these addresses is verified by means of <b>digital certificates</b> based on asymmetric algorithms, that is, by means of a system of public key and private key.</p>
            <p>So, we can say that bitcoin is a cryptographically protected system where everyone keeps everyone's accounts.</p>

            <h3 class="lead">4. What is bitcoin mining?</h3>
            <p>Mining bitcoins is the process of investing computational capacity to process transactions, ensure network security and get all participants in sync.</p>
            <p>The bitcoin algorithm allows a collective record to be kept of all transactions across the network. This public and shared record among all parties is known as <b>blockchain. </b> In this chain all transactions are recorded with the date, time, digital signature of the participants and amount transferred.</p>
            <p>However, several transactions occur at the same time, so it is necessary to <b>agree on an order</b> to keep the log synchronized and updated among all the nodes in the network. For this, each time a transaction occurs all nodes in the network verify that it is viable and authentic.</p>
            <p>Every 10 minutes (approximately) all nodes store in a block the valid transactions received. However, not all nodes add these transactions in the same order, so it is necessary to define which block will be taken as valid and added to the registry (blockchain).</p>
            <p>For this, all the nodes in the network participate in a sort of contest. It consists of finding the block that, added to a random number and applied to a cryptographic function, results in a hash that meets one characteristic: having a certain number of leading zeros.</p>
            <p>Since it is impossible to predict the result of a hash function, each miner must calculate the hash for his block of transactions added to the random number <b>until a valid result is found. </b> As the number of leading zeros required to validate the information increases, the difficulty of finding a hash that meets this requirement also increases.</p>
            <p>If we think about how blockchain works, mining consists of using CPU and GPU resources to perform these cryptographic mathematical calculations, which consume a lot of processing.</p>
            <p>Each time the calculation of a block is solved and added to the chain, <b>12.5 bitcoins are distributed among the miners </b>who participated in obtaining that block. In this way, there are nodes motivated to work in the processing of transactions and new coins are also introduced to the market.</p>
            <p>Although the process seems simple, the cryptocurrency is designed in such a way that mining becomes more difficult and requires more computational power and time to obtain.</p>
            
